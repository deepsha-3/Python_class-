# ef-else statement example:
marks= int(input("Enter your marks:"))

if(marks>=90):
    print("Grade A+")

elif(marks>=80):
    print("Grade A")

elif(marks>=70):
    print("Grade B+")

elif(marks>=60):
    print("Grade C+")

elif(marks>=50):
    print("Grade C")

elif(marks>=40):
    print("Grade D")

else:
    print("Sorry!, You are fail.")
