
# List methods 

List = ["a", "b", "c", "d"]
print("Original List:", List)

# Append method
List.append("z")
print("After Append:", List)

# insert method
List.insert(3, "p")
print("After Insert:", List)

# sort method
List.sort()
print("After Sort:", List)

# remove method
List.remove("a")
print("After Remove:", List)

# reverse method
List.reverse()
print("After Reverse:", List)

# min value 
Value = min(List)
print("Min Value:", Value)

# max value
Value1 = max(List)
print("Max Value:", Value1)