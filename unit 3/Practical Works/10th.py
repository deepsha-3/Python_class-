# Write a program to make use of string manipulation methods and also perform string operations.

# string
text = "Hello, coders!"

print("This is the original text:", text)

# string manipulations

# convert uppercase
upper_text = text.upper()
print("This is the text in uppercase:", upper_text)

# convert lowercase
lower_text = text.lower()
print("This is the text in lowercase:", lower_text)

# convert title case
title_text = text.title()
print("This is the text in title case:", title_text)
