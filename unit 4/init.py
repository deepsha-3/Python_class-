class Car:
    def __init__(self, name, color):
        self.name = name
        self.color = color



car1= Car("BMW", "Black")
print("The car name is:", car1.name)
print("The car color is:", car1.color )

car2= Car("Audi", "Black")
print("The car name is:", car2.name)
print("The car color is:", car2.color )


